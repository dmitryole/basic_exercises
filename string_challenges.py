# Вывести количество гласных букв в слове
word = 'Архангельск'
gls = ["а", "е", "ё", "и", "о", "у", "ы", "э", "ю", "я"]
list_word = list(word.lower())
sum_glas = 0
for i in list_word:
    if i in gls:
        sum_glas += 1
print(f'Вывести количество гласных букв в слове: {sum_glas}')


