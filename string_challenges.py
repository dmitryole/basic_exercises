# Вывести усреднённую длину слова в предложении
sentence = 'Мы приехали в гости'
summ = 0
for i in sen:
    summ += len(i)
avg = summ / len(sen)
print(f'Вывести усреднённую длину слова в предложении: {round(avg)}')